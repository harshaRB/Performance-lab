  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  unique(user_id, date)
);

alter table nutrition_logs enable row level security;

drop policy if exists "Users can all on nutrition_logs" on nutrition_logs;
create policy "Users can all on nutrition_logs"
  on nutrition_logs for all
  using ( auth.uid() = user_id );

-- 4. TRAINING LOGS (Workouts)
create table if not exists training_logs (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users not null,
  date date not null,
  exercise_name text not null,
  sets integer default 0,
  reps integer default 0,
  weight numeric default 0,
  volume_load numeric generated always as (sets * reps * weight) stored,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

alter table training_logs enable row level security;

drop policy if exists "Users can all on training_logs" on training_logs;
create policy "Users can all on training_logs"
  on training_logs for all
  using ( auth.uid() = user_id );
